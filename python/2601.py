from common import List

# calculate primes within [1, 1000]
valid = [True] * 1001
valid[0] = valid[1] = False
for i in range(2, len(valid)):
    if valid[i]:
        for j in range(i * i, len(valid), i):
            valid[j] = False
primes = [i for i in range(len(valid)) if valid[i]]


class Solution:
    def primeSubOperation(self, nums: List[int]) -> bool:

        n = len(nums)
        if n == 1:
            return True

        for i in range(n - 2, -1, -1):
            if nums[i] < nums[i + 1]:
                continue

            difference = nums[i] - nums[i + 1]
            min_prime = self.find_min_prime(difference)
            nums[i] -= min_prime
            if nums[i] <= 0:
                return False

        return True

    @staticmethod
    def find_min_prime(lower_bound: int) -> int:
        candidate = lower_bound + 1
        while candidate < 1000:
            if candidate in primes:
                return candidate
            else:
                candidate += 1

        return 1000


